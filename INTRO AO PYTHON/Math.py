print(math.ceil(pi))
# return only the number without the decimal places
print(math.floor(pi))
print(abs(number))  # output 2 will convert negative nmber to positive
print(round(math.pow(2, 3)))  # output 8
print(round(math.sqrt(25)))  # output 5
print(max(numbers))  # output 3
